import UIKit

/// Centralizes every color useful for the application and extends UIColor
extension UIColor {
    
    static let cellBackground: UIColor = UIColor(named: "CellBackground")!
    static let cellBorder: UIColor = UIColor(named: "CellBorder")!
    static let cellTitle: UIColor = UIColor(named: "CellTitle")!
    static let cellSubTitle: UIColor = UIColor(named: "CellSubTitle")!
    static let profilePictureBorder: UIColor = UIColor(named: "ProfilePictureBorder")!
    static let profilePictureBackground: UIColor = UIColor(named: "ProfilePictureBackground")!
    static let navigationTitle: UIColor = UIColor(named: "NavigationTitle")!
    static let userDetailsCellText: UIColor = UIColor(named: "DetailsCellText")!
}
